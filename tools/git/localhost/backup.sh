# >>>> Environment

setEnvironment() {
  echo "---------------------------------------------------------------------------------------------------------------"
  echo "[ ENV ] ${PROCESSOR_TYPE} - ${PLATFORM_TYPE} - Environment"
  echo "---------------------------------------------------------------------------------------------------------------"
  PS3="Menu: "
  select num in "dev" "exit"; do
    case "$REPLY" in
    1)
      # >>>> Dev Environment
      ENVIRONMENT_NAME="dev"
      break
      ;;
    2)
      echo "exit()"
      setEnd
      ;;
    *)
      echo "[ ERROR ] Unknown Command"
      setEnd
      ;;
    esac
  done

  echo
  echo "- PROJECT ENV : ${ENVIRONMENT_NAME}"
  echo
}

# >>>> Platform

setPlatform() {
  echo "---------------------------------------------------------------------------------------------------------------"
  echo "[ $(echo ${ENVIRONMENT_NAME} | tr '[a-z]' '[A-Z]') ] ${PROCESSOR_TYPE} - ${PLATFORM_TYPE} - Platform"
  echo "---------------------------------------------------------------------------------------------------------------"
  echo "- PLATFORM OS : ${PLATFORM_TYPE}"
  echo

  # >>>> Environment
  if [ "${ENVIRONMENT_NAME}" == "dev" ]; then

    # >>>> Platform
    if [ "${PLATFORM_TYPE}" == "Linux" ]; then
      # ------------------------------------------------------------------------------------------------------------------
      # Platform - Linux - Ubuntu
      # ------------------------------------------------------------------------------------------------------------------
      # >>>> Processer
      echo ">>>> Linux - Process - Supervisor"
      echo
      sudo supervisorctl stop messenger-consume:*
      echo

      sudo supervisorctl status
      echo

    elif [ "${PLATFORM_TYPE}" == "Darwin" ]; then
      # ------------------------------------------------------------------------------------------------------------------
      # Platform - MacOS
      # ------------------------------------------------------------------------------------------------------------------
      # >>>> Processer
      echo ">>>> MacOS - Processer - Supervisor"
      echo
      supervisorctl stop messenger-consume:*
      echo

      sudo supervisorctl status
      echo

    elif [ "${PLATFORM_TYPE}" == "Windows" ]; then
      # ------------------------------------------------------------------------------------------------------------------
      # Platform - Windows
      # ------------------------------------------------------------------------------------------------------------------
      # >>>> Processer
      echo ">>>> Windows - Processer - Supervisor"
      echo
      supervisorctl stop messenger-consume:*
      echo

      sudo supervisorctl status
      echo

    else
      echo "Please check Operating System"
      setExit
    fi

  fi
}

# >>>> Project

setProject() {
  echo "---------------------------------------------------------------------------------------------------------------"
  echo "[ $(echo ${ENVIRONMENT_NAME} | tr '[a-z]' '[A-Z]') ] ${PROCESSOR_TYPE} - ${PLATFORM_TYPE} - Project"
  echo "---------------------------------------------------------------------------------------------------------------"
  echo "- PROJECT NAME : ${PROJECT_NAME}"
  echo

  # >>>> Project - App
  if [ -d app ]; then
    (
      cd app || return

      # >>>> App - Symfony Command                                             https://symfony.com/doc/current/deployment.html
      if [ -f bin/console ]; then

        # >>>> Environment
        if [ "${ENVIRONMENT_NAME}" == "dev" ]; then

          # >>>> Platform
          if [ "${PLATFORM_TYPE}" == "Linux" ]; then
            # ----------------------------------------------------------------------------------------------------------
            # Platform - Linux - Ubuntu
            # ----------------------------------------------------------------------------------------------------------

            echo ">>>> App - Symfony - Messenger"
            echo
            symfony console messenger:stop-workers
            echo

            echo ">>>> App - Symfony - Local Server"
            echo
            symfony local:server:stop
            echo

          elif [ "${PLATFORM_TYPE}" == "Darwin" ]; then
            # ----------------------------------------------------------------------------------------------------------
            # Platform - MacOS
            # ----------------------------------------------------------------------------------------------------------

            echo ">>>> App - Symfony - Messenger"
            echo
            symfony console messenger:stop-workers
            echo

            echo ">>>> App - Symfony - Local Server"
            echo
            symfony local:server:stop
            echo

          elif [ "${PLATFORM_TYPE}" == "Windows" ]; then
            # ----------------------------------------------------------------------------------------------------------
            # Platform - Windows
            # ----------------------------------------------------------------------------------------------------------

            echo ">>>> App - Symfony - Messenger"
            echo
            symfony console messenger:stop-workers
            echo

            echo ">>>> App - Symfony - Local Server"
            echo
            symfony local:server:stop
            echo

          else
            echo "Please check Operating System"
            setExit
          fi

        fi

      fi
    )
  else
    echo
    echo "[ ERROR ] There is not a folder : app"
    echo
    setExit
  fi

  # >>>> Project - Base - Import a project file
  if [ -f ${PROJECT_PATH}/scripts/base/_project.sh ]; then
    source ${PROJECT_PATH}/scripts/base/_project.sh
  else
    echo "Please check a file : ${PROJECT_PATH}/scripts/base/_project.sh" && exit
  fi

  # >>>> Project - Content - Git
  echo ">>>> Git - Project"
  echo

  DEFAULT_BRANCH=$(git config --get init.defaultBranch)
  TODAY=$(date "+%Y-%m-%d")

  # 1. Update all of the files and commit
  git add -A
  git commit -m "Backup  ${TODAY}"
  echo

  # 2. Push it to main branch
  git push -f origin main
  echo

  # 3. Show logs
  git log -5 --graph --date=short
  echo
}


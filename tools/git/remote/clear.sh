# ----------------------------------------------------------------------------------------------------------------------
# Tools
# ----------------------------------------------------------------------------------------------------------------------

setGit() {
  echo "---------------------------------------------------------------------------------------------------------------"
  echo "[ $(echo ${ENVIRONMENT_NAME} | tr '[a-z]' '[A-Z]') ] ${PROCESSOR_TYPE} - ${PLATFORM_TYPE} - Tools - Git"
  echo "---------------------------------------------------------------------------------------------------------------"
  # >>>> Git - Clear history
  if [ -d .git ]; then
    sudo rm -rf .git
    echo

    git init
    echo

    git add .
    echo

    git commit -m "${GIT_CONFIG_GLOBAL_USER_NAME}"
    echo

    git remote add origin ${GIT_REMOTE_ORIGIN_URL}
    echo

    git push --mirror --force
    echo

    git status
  fi
  echo
}


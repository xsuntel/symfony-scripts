# Scripts
# ----------------------------------------------------------------------------------------------------------------------

setCloud() {
  echo "---------------------------------------------------------------------------------------------------------------"
  echo "[ $(echo ${ENVIRONMENT_NAME} | tr '[a-z]' '[A-Z]') ] ${PROCESSOR_TYPE} - ${PLATFORM_TYPE} - Scripts - Cloud"
  echo "---------------------------------------------------------------------------------------------------------------"
  # >>>> Git - Remote
  git remote -v
  echo

  # >>>> Git - Config - Global                                                                  https://git-scm.com/docs
  git config --global --list | grep -i "user."
  echo

  echo
}


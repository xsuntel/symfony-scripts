# App
# ----------------------------------------------------------------------------------------------------------------------

setPhp() {
  echo "---------------------------------------------------------------------------------------------------------------"
  echo "[ $(echo ${ENVIRONMENT_NAME} | tr '[a-z]' '[A-Z]') ] ${PROCESSOR_TYPE} - ${PLATFORM_TYPE} - App - PHP"
  echo "---------------------------------------------------------------------------------------------------------------"
  # >>>> Platform
  if [ "${PLATFORM_TYPE}" == "Linux" ]; then
    # ------------------------------------------------------------------------------------------------------------------
    # Linux - Ubuntu
    # ------------------------------------------------------------------------------------------------------------------
    # >>>> PHP
    echo ">>>> PHP"
    php -v
    echo

    php -ini | egrep "Loaded Configuration File"
    echo

    # >>>> PHP-FPM - configuration
    echo ">>>> PHP-FPM"
    systemctl status php${PHP_VERSION}-fpm --no-pager
    echo

    php-fpm${PHP_VERSION} -m
    echo

    php-fpm${PHP_VERSION} -t
    echo

    # >>>> PHP - Composer
    echo ">>>> PHP - Composer"
    echo
    if [ -f /usr/local/bin/composer ]; then
      echo "Composer  : $(composer --version)"
    fi
  fi
  echo
}


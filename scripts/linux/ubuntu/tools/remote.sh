# >>>> Environment

setEnvironment() {
  echo "---------------------------------------------------------------------------------------------------------------"
  echo "[ ENV ] ${PROCESSOR_TYPE} - ${PLATFORM_TYPE}"
  echo "---------------------------------------------------------------------------------------------------------------"
  # >>>> Select one of some environments
  PS3="Menu: "
  select num in "dev" "exit"; do
    case "$REPLY" in
    1)
      # >>>> DEV Environment
      ENVIRONMENT_NAME="dev"
      break
      ;;
    2)
      echo "exit()"
      setEnd
      ;;
    *)
      echo "[ ERROR ] Unknown Command"
      setEnd
      ;;
    esac
  done
  echo
  echo "- PROJECT ENV : ${ENVIRONMENT_NAME}"
  echo
}

# >>>> Platform

setPlatform() {
  echo "---------------------------------------------------------------------------------------------------------------"
  echo "[ $(echo ${ENVIRONMENT_NAME} | tr '[a-z]' '[A-Z]') ] ${PROCESSOR_TYPE} - ${PLATFORM_TYPE} - Platform"
  echo "---------------------------------------------------------------------------------------------------------------"
  echo "- PLATFORM OS : ${PLATFORM_TYPE}"
  echo -n
  # >>>> Platform
  if [ "${PLATFORM_TYPE}" == "Linux" ]; then

    if [ "${ENVIRONMENT_NAME}" == "dev" ]; then
      # ----------------------------------------------------------------------------------------------------------------
      # RDP
      # ----------------------------------------------------------------------------------------------------------------
      # >>>> Install the related packages
      sudo apt-get -y install xrdp xfce4
      sudo systemctl enable xrdp

      # >>>> Add a user
      sudo adduser xrdp ssl-cert

      # >>>> xfce4
      echo xfce4-session >~/.xsession

      # >>>> Update firewal
      sudo vi /etc/xrdp/xrdp.ini
      # port=192.168.0.100:3389

      # >>>> Restart the service
      sudo systemctl restart xrdp

      # >>>> Check network ports
      sudo netstat -plnt | grep xrdp

      # >>>> Check a log
      tail -n 50 /var/log/syslog


      # sudo vi /etc/xrdp/startwm.sh

      # unset DBUS_SESSION_BU ADDRESS
      # unset XDG_RUNTIME_DIR
      #
      # test -x /etc/X11/Xsession && exec /etc/X11/Xession            <----   주석
      # exec /bin/sh /etc/X11/Xession                                 <----   주석
      #
      # test -x /usr/bin/startxfce4 && exec /usr/bin/startxfce4
      # exec /bin/sh /usr/bin/startxfce4

      # ----------------------------------------------------------------------------------------------------------------
      # Firewall
      # ----------------------------------------------------------------------------------------------------------------
      # >>>> Import a firewall file
      if [ -f ${PROJECT_PATH}/scripts/linux/ubuntu/settings/firewall/_remote.sh ]; then
        source ${PROJECT_PATH}/scripts/linux/ubuntu/settings/firewall/_remote.sh
      else
        echo "Please check a file : ${PROJECT_PATH}/scripts/linux/ubuntu/settings/firewall/_remote.sh" && exit
      fi
      echo

    fi
  elif [ "${PLATFORM_TYPE}" == "Darwin" ]; then
    # ------------------------------------------------------------------------------------------------------------------
    # MacOS
    # ------------------------------------------------------------------------------------------------------------------
    if [ "${ENVIRONMENT_NAME}" == "dev" ]; then
      # ----------------------------------------------------------------------------------------------------------------
      # RDP
      # ----------------------------------------------------------------------------------------------------------------
      # >>>> Install the related packages
      echo

    fi
  elif [ "${PLATFORM_TYPE}" == "Windows" ]; then
    # ------------------------------------------------------------------------------------------------------------------
    # Windows
    # ------------------------------------------------------------------------------------------------------------------
    if [ "${ENVIRONMENT_NAME}" == "dev" ]; then
      # ----------------------------------------------------------------------------------------------------------------
      # RDP
      # ----------------------------------------------------------------------------------------------------------------
      # >>>> Install the related packages
      echo

    fi
  else
    echo "Please check Operating System"
    setEnd
  fi
  echo
}


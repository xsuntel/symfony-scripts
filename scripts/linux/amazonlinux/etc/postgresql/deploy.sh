setPostgreSQL() {
  echo "---------------------------------------------------------------------------------------------------------------"
  echo "[ $(echo ${ENVIRONMENT_NAME} | tr '[a-z]' '[A-Z]') ] ${PROCESSOR_TYPE} - ${PLATFORM_TYPE} - Database - PostgreSQL"
  echo "---------------------------------------------------------------------------------------------------------------"
  if [ "${ENVIRONMENT_NAME}" == "dev" ]; then
    # >>>> Linux
    if [ "${PLATFORM_TYPE}" == "Linux" ]; then
      # >>>> PostgreSQL
      local POSTGRESQL_STATUS
      POSTGRESQL_STATUS=$(systemctl is-active postgresql)
      if [ "${POSTGRESQL_STATUS}" == "enabled" ]; then
        sudo systemctl is-active postgresql
      else
        (
          cd /tmp || return

          echo "- Install packages"
          sudo apt-get install -y postgresql
          echo

          echo "- Enable this service"
          sudo systemctl enable postgresql
          echo
        )
      fi
    fi
  fi
  echo
}


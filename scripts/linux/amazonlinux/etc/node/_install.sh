#!/bin/bash
# ======================================================================================================================
# Scripts - VM - Amazon Linux - Node - Install
# ======================================================================================================================
# >>>> Platform
if [ "${PLATFORM_TYPE}" == "Linux" ]; then
  # --------------------------------------------------------------------------------------------------------------------
  # Install the packages
  # --------------------------------------------------------------------------------------------------------------------
  # >>>> Node                                                                              https://github.com/nvm-sh/nvm
  sudo dnf install -y nodejs --skip-broken

  # >>>> NPM  - Install YARN                              https://classic.yarnpkg.com/lang/en/docs/install/#mac-stable
  npm install --global yarn --force
  echo

  # --------------------------------------------------------------------------------------------------------------------
  # Update the configuration
  # --------------------------------------------------------------------------------------------------------------------
  # >>>> Update webpack-encore
  (
    cd app || return

    if [ ! -f package.json ]; then
      # >>>> NPM : Create package.json
      npm init -y
      echo
    fi

  )

  echo
  echo "---------------------------------------------------------------------------------------------------------------"
  echo
fi

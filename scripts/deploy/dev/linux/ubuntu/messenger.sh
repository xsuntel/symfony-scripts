setVM() {
  echo "---------------------------------------------------------------------------------------------------------------"
  echo "[ ${ENVIRONMENT_NAME^^} ] ${PLATFORM_TYPE} - VM ( Instance )"
  echo "---------------------------------------------------------------------------------------------------------------"
  echo

  # >>>> Migrate
  if [ -d app ]; then
    (
      cd app || return
      # >>>> PHP - Symfony Command
      if [ -f bin/console ]; then

        # >>>> Select one of some environments
        PS3="Menu: "
        select num in "status" "start" "stop" "exit"; do
          case "$REPLY" in
          1)
            SUPERVISOR_COMMAND="status"
            break
            ;;
          2)
            SUPERVISOR_COMMAND="start"
            break
            ;;
          3)
            SUPERVISOR_COMMAND="stop"
            break
            ;;
          4)
            echo "exit()"
            exit
            ;;
          *)
            echo "[ ERROR ] Unknown Command"
            exit
            ;;
          esac
        done
        echo

        # --------------------------------------------------------------------------------------------------------------
        # 1) Status
        # --------------------------------------------------------------------------------------------------------------
        if [ "${SUPERVISOR_COMMAND}" == "status" ]; then

            supervisorctl status
            echo

        # --------------------------------------------------------------------------------------------------------------
        # 2) Start
        # --------------------------------------------------------------------------------------------------------------
        elif [ "${SUPERVISOR_COMMAND}" == "start" ]; then

            supervisorctl start messenger-consume:*
            echo

            supervisorctl status
            echo

        # --------------------------------------------------------------------------------------------------------------
        # 3) Stop
        # --------------------------------------------------------------------------------------------------------------
        elif [ "${SUPERVISOR_COMMAND}" == "stop" ]; then

            supervisorctl stop messenger-consume:*
            echo

            supervisorctl status
            echo

        fi

      else
        echo "[ ERROR ] There is not a command : app/bin/console"
        exit
      fi
    )
  else
    echo "[ ERROR ] There is not a folder : app"
    setExit
  fi
  echo
}


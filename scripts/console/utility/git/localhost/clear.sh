# >>>> App

setPhp() {
  echo "---------------------------------------------------------------------------------------------------------------"
  echo "[ ${ENVIRONMENT_NAME^^} ] ${PLATFORM_TYPE} - Base - App - Packages"
  echo "---------------------------------------------------------------------------------------------------------------"
  echo

  # >>>> Project - App
  if [ -d app ]; then
    (
      cd app || return

      # >>>> PHP - Symfony Command                                        https://symfony.com/doc/current/deployment.html
      if [ -f bin/console ]; then

        # >>>> Environment
        if [ "${ENVIRONMENT_NAME}" == "dev" ]; then

          echo ">>>> App - PHP - Symfony - Messenger"
          echo

          symfony console messenger:stop-workers
          echo

          echo ">>>> App - PHP - Symfony - Local Server"
          echo
          symfony local:server:stop
          echo

        fi

        # >>>> public
        if [ -d public ]; then
          # >>>> public/assets
          if [ -d public/assets ]; then
            rm -rf public/assets/*
          fi
          # >>>> public/bundles
          if [ -d public/bundles ]; then
            rm -rf public/bundles/*
          fi
          # >>>> public/var
          if [ -d public/var ]; then
            rm -rf public/var/*
          fi
          # >>>> Remove related performance files
          if [ -f public/0.meta.json ]; then
            rm -f public/[0-9]
            rm -f public/[0-9].meta
            rm -f public/[0-9].meta.json
          fi
        fi

        # >>>> var
        if [ -d var/cache ]; then
          rm -rf var/cache
        fi
        if [ -d var/log ]; then
          rm -rf var/log
        fi
        if [ -d var/sessions ]; then
          rm -rf var/sessions
        fi
        if [ -d var/translations ]; then
          rm -rf var/translations
        fi

        # >>>> vendor
        if [ -d vendor ]; then
          rm -rf ./vendor
        fi

        # >>>> package
        if [ -f package.json ]; then
          rm -f package.json
        fi

      fi
    )
  else
    echo
    echo "[ ERROR ] There is not a folder : app"
    echo
    setExit
  fi
}

